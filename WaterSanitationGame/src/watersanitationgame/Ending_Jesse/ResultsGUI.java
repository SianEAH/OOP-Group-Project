    private void nextBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextBTNActionPerformed
        int choice1;
        int choice2;
        String saveName;
        
        
        if (counter != 5){
            counter++;
            displayText();
        }else{
            if (!pastSave){
                choice1 = JOptionPane.showConfirmDialog(null, "Do you want to save your results?");
                if (choice1 == 0){
                    saveName = JOptionPane.showInputDialog("What do you want to name your save?");
                    currentSave.setSaveName(saveName);
                    
                    endingCalculation.save();
                }else if(choice1 == 1)
                {
                    endingCalculation.getsList().remove(currentSave);
                }
            
                choice2 = JOptionPane.showConfirmDialog(null, "Do you want to quit?", null , 1);

                if (choice2 == 0){
                    System.exit(0);
                }else  if (choice2 == 1) {
                    WelcomeGUI welcomeGUI = new WelcomeGUI();
                    welcomeGUI.setVisible(true);
                    dispose();
                }
            
            }else
            {
                dispose();
            }

        }
         
    }//GEN-LAST:event_nextBTNActionPerformed

    
    private void displayText(){
        String text = "";
        
        text += "EVENT: ";
        
        switch (counter) {
            case 1 -> {
                text += "Firstly, you receive a report stating that safety standards in water treatment plants around the country are below standard.";
                text += "\n\n\n";
                text += "CHOICE: ";
                if (currentSave.isBeganSafetyInspections()){
                    text += "You chose to conduct safty inspections";
                }else{
                    text += "You chose to not conduct safty inspections";
                }       text += "\n\n\n";
                text += "EXPLANATION: ";
                text += "It is important that saftey inspections are made regularly to ensure our water quality is up to standard";
            }
            case 2 -> {
                text += "Some time later, you are invited to give a couple words to the press about your new position, \nthey ask for a quote about what kind of policy you might enact";
                text += "\n\n\n";
                text += "CHOICE: ";
                if (currentSave.isInterviewAnswer()){
                    text += "You chose to be honest with the media";
                }else{
                    text += "You chose to lie to the media";
                }       text += "\n\n\n";
                text += "EXPLANATION: ";
                text += "Transparency with the public is an important principle all public officals should hold to.";
            }
            case 3 -> {
                text += "A couple months later, you receive an alarming report. The water flowing to a specific town has been contaminated,\n due to a sewage network overflowing nearby a water distribution channel. What shall be done about this?";
                text += "\n\n\n";
                text += "CHOICE: ";
                if (currentSave.isRedirectedWater()){
                    text += "You chose to redirect the water flow ensuring clean water";
                }else{
                    text += "You chose to fix the sewage pipes";
                }       text += "\n\n\n";
                text += "EXPLANATION: ";
                text += "Although the sweage pipes needed to be repaired, it was more important to ensure that the town had clean water";
            }
            case 4 -> {
                text += "As you prepare to leave the facility, the manager of it shakes your hand and slips a thick envelope into it with a smile. \nOnce outside you manage to check the contents and find a large amount of money, \nenough to keep your personal bills and responsibilities  in check for a season...";
                text += "\n\n\n";
                text += "CHOICE: ";
                if (!currentSave.isTookBribe()){
                    text += "You chose to take a bribe";
                }else{
                    text += "You rejected the bribe";
                }       text += "\n\n\n";
                text += "EXPLANATION: ";
                text += "Bribery is a very large concern when it comes to governing bodies. Bribes can cause extreme damage to \ninstituations that cause ordinary people to suffer.";
            }
            case 5 -> {
                text += "for the next few months, you find yourself dealing with a massive amount of paperwork, so much so that you realise \nyou are spending almost no time at home or with family, rarely relaxing. You think about what you might do about it all?";
                text += "\n\n\n";
                text += "CHOICE: ";
                if (currentSave.isStayedInOffice()){
                    text += "You chose grind out the paper work";
                }else{
                    text += "You chose to go on vacation with your family";
                }       text += "\n\n\n";
                text += "EXPLANATION: ";
                text += "Although it is important to relax now and again, public officals have the resonsability of many people \non their shoulders and should therefore ensure their work is finished before taking personal time.";
            }
            default -> {
            }
        }
       
       eventTA.setText(text);
   }  
    
