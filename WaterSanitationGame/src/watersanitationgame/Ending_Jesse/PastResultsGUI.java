    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        
        boolean foundSearch = false;
        StringBuffer sBuff = new StringBuffer();
        
        if (playernameRB.isSelected()){
            
            String searchTerm = JOptionPane.showInputDialog("Enter in name to search");
            
            for (Save tempS: sList){
                if (tempS.getName().equalsIgnoreCase(searchTerm)){
                    foundSearch = true;
                    if (displaySaveJOption(tempS)){
                        break;
                    }
                }
            }
            
        }else if (savenameRB.isSelected()){
            String searchTerm = JOptionPane.showInputDialog("Enter in country to search");
            
            for (Save tempS: sList){
                if (tempS.getSaveName().equalsIgnoreCase(searchTerm)){
                    foundSearch = true;
                    if (displaySaveJOption(tempS)){
                        break;
                    }
                }
            }
        } else if (countryRB.isSelected()){
            String searchTerm = JOptionPane.showInputDialog("Enter in country to search");
            
            for (Save tempS: sList){
                if (tempS.getCountry().equalsIgnoreCase(searchTerm)){
                    foundSearch = true;
                    if (displaySaveJOption(tempS)){
                        break;
                    }
                }
            }
        }
        
        if (!foundSearch){
            displayTA.setText("There are no saves with that search");
        }
  
    }//GEN-LAST:event_searchBTNActionPerformed

